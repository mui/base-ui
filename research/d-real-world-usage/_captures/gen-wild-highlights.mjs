// Generates apps/storybook/src/stories/shared/wildHighlights.ts from highlight-manifest.json.
// Two kinds of entry:
//   - per-component `component::domain` (a showcase site's own component page), and
//   - site-level `*::domain` (a landing-page capture, used as a fallback for any card on
//     that site without a per-component highlight).
//   node research/d-real-world-usage/_captures/gen-wild-highlights.mjs

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const dir = path.dirname(fileURLToPath(import.meta.url));
const outPath = path.resolve(dir, '../../../apps/storybook/src/stories/shared/wildHighlights.ts');
const IMPORT_PREFIX = '../../../../../research/d-real-world-usage/_captures/';

const SITEKEY = { '9ui.dev': '9ui', 'reui.io': 'reui', 'coss.com': 'coss', 'lumiui.dev': 'lumiui' };
// Base UI components captured per-component (the MATRIX keys in gen-component-targets.mjs).
const KNOWN = new Set([
  'accordion',
  'alert-dialog',
  'autocomplete',
  'avatar',
  'button',
  'checkbox-group',
  'checkbox',
  'collapsible',
  'combobox',
  'context-menu',
  'dialog',
  'drawer',
  'field',
  'fieldset',
  'form',
  'input',
  'menu',
  'menubar',
  'meter',
  'navigation-menu',
  'number-field',
  'otp-field',
  'popover',
  'preview-card',
  'progress',
  'radio',
  'scroll-area',
  'select',
  'separator',
  'slider',
  'switch',
  'tabs',
  'toast',
  'toggle-group',
  'toggle',
  'toolbar',
  'tooltip',
]);

const manifest = JSON.parse(fs.readFileSync(path.join(dir, 'highlight-manifest.json'), 'utf8'));
const captured = manifest.filter((e) => e.fileHighlight);
const camel = (s) => `img_${s.replace(/[^a-z0-9]+/gi, '_').replace(/^_+|_+$/g, '')}`;

let imports = '';
const byKey = new Map();
for (const e of captured) {
  const sk = SITEKEY[e.domain];
  let component = null;
  if (sk) {
    const c = e.slug.replace(new RegExp(`^${sk}-`), '').replace(/-hl$/, '');
    if (KNOWN.has(c)) {
      component = c;
    }
  }
  const key = component ? `${component}::${e.domain}` : `*::${e.domain}`;
  // Per-component wins over site-level for the same key; first writer wins otherwise.
  if (byKey.has(key)) {
    continue;
  }
  const vBase = camel(`${e.slug}_page`);
  const vHi = camel(`${e.slug}_hi`);
  imports += `import ${vBase} from '${IMPORT_PREFIX}${e.file}';\n`;
  imports += `import ${vHi} from '${IMPORT_PREFIX}${e.fileHighlight}';\n`;
  byKey.set(key, { vBase, vHi, e });
}

let body = '';
for (const [key, r] of [...byKey.entries()].sort()) {
  body +=
    `  ${JSON.stringify(key)}: {\n` +
    `    image: ${r.vBase},\n` +
    `    highlightImage: ${r.vHi},\n` +
    `    selector: ${JSON.stringify(r.e.selector)},\n` +
    `    route: ${JSON.stringify(r.e.route)},\n` +
    `    pageUrl: ${JSON.stringify(r.e.url)},\n` +
    `    matchedRole: ${JSON.stringify(r.e.matchedRole ?? null)},\n` +
    `  },\n`;
}

const out = `// AUTO-GENERATED by research/d-real-world-usage/_captures/gen-wild-highlights.mjs — do not
// edit by hand. Maps a docs "In the wild" card (Base UI component + site domain) to the
// screenshot + spotlight highlight captured from that site. \`WildCard\` looks entries up via
// the \`component\` prop on the enclosing \`WildCards\` plus the card's own \`live\`/\`href\`
// domain, falling back to a site-level (\`*::domain\`) capture when there's no per-component one.
/* eslint-disable import/no-relative-packages */
${imports.trimEnd()}
/* eslint-enable import/no-relative-packages */

export interface WildHighlight {
  /** The page screenshot (same frame as the highlight). */
  image: string;
  /** The spotlight screenshot (page dimmed, component spotlighted). */
  highlightImage: string;
  /** Concrete CSS selector to the component on the page. */
  selector: string;
  /** URL pathname of the page. */
  route: string;
  /** Full page URL the screenshots came from. */
  pageUrl: string;
  /** ARIA role of the spotlighted element at capture time (null if it had none). */
  matchedRole: string | null;
}

/**
 * ARIA role of a site-level capture → the Base UI component names it can legitimately
 * depict. Used to gate the site-level (\`*::domain\`) fallback so a landing-page capture is
 * only shown on a card whose component it actually shows (e.g. a \`combobox\`-role capture is
 * a valid stand-in for a Select card, but not for a Radio card).
 */
const ROLE_COMPONENTS: Record<string, readonly string[]> = {
  tablist: ['tabs'],
  tab: ['tabs'],
  menubar: ['menubar'],
  radiogroup: ['radio'],
  radio: ['radio'],
  combobox: ['select', 'combobox', 'autocomplete'],
  listbox: ['select', 'combobox', 'autocomplete'],
  checkbox: ['checkbox', 'checkbox-group'],
  switch: ['switch'],
  slider: ['slider'],
  meter: ['meter'],
  progressbar: ['progress'],
  menu: ['menu'],
  dialog: ['dialog', 'alert-dialog'],
  toolbar: ['toolbar'],
};

/** Keyed by \`\${component}::\${domain}\` or the site-level \`*::\${domain}\`. */
export const wildHighlights: Record<string, WildHighlight> = {
${body.trimEnd()}
};

/** Normalize a card's \`live\`/\`href\` URL to the bare domain used as the registry key. */
export function highlightDomain(url: string | undefined): string | null {
  if (!url) {
    return null;
  }
  try {
    return new URL(url).hostname.replace(/^www\\./, '');
  } catch {
    return null;
  }
}

export function lookupHighlight(
  component: string | undefined,
  url: string | undefined,
): WildHighlight | undefined {
  const domain = highlightDomain(url);
  if (!domain) {
    return undefined;
  }
  // A per-component capture is targeted at that component's own page, so it is always
  // correct for its card.
  const exact = component ? wildHighlights[\`\${component}::\${domain}\`] : undefined;
  if (exact) {
    return exact;
  }
  // The site-level fallback is a single landing-page capture that shows one arbitrary
  // component; only serve it when the component it actually depicts (its ARIA role) matches
  // the card's component — otherwise a card would show the wrong component.
  const site = wildHighlights[\`*::\${domain}\`];
  if (site && component) {
    const shows = site.matchedRole ? ROLE_COMPONENTS[site.matchedRole] : undefined;
    if (shows && shows.includes(component)) {
      return site;
    }
  }
  return undefined;
}
`;

fs.writeFileSync(outPath, out);
const perComp = [...byKey.keys()].filter((k) => !k.startsWith('*::')).length;
const siteLevel = [...byKey.keys()].filter((k) => k.startsWith('*::')).length;
console.log(
  `wrote ${byKey.size} registry entries (${perComp} per-component, ${siteLevel} site-level)`,
);
