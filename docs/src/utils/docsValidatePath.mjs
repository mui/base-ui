export const SECTION_INDEX_FILE_NAME = 'index.mdx';

/**
 * Converts an app-relative docs path to the argument used by `pnpm docs:validate`.
 *
 * @param {string} appRelativePath
 */
export function toDocsValidatePath(appRelativePath) {
  return appRelativePath
    .replace(/\\/g, '/')
    .replace(/^(src\/app\/|app\/)/, '')
    .replace(/\/(page|index)\.mdx$/, '');
}

/**
 * Formats a validate path for shell usage.
 *
 * @param {string} appRelativePath
 */
export function formatDocsValidateCommand(appRelativePath) {
  const validatePath = toDocsValidatePath(appRelativePath);
  const quotedPath = /[()]/.test(validatePath) ? `"${validatePath}"` : validatePath;
  return `pnpm docs:validate ${quotedPath}`;
}

/**
 * Updates the autogenerated validate marker in section index markdown.
 *
 * @param {string} markdown
 * @param {string} appRelativePath
 */
export function updateSectionIndexValidateMarker(markdown, appRelativePath) {
  const command = formatDocsValidateCommand(appRelativePath);
  const marker = `[//]: # 'This section is autogenerated, DO NOT EDIT AFTER THIS LINE, run: ${command}'`;

  return markdown.replace(
    /\[\/\/\]: # 'This section is autogenerated, DO NOT EDIT AFTER THIS LINE, run: pnpm docs:validate .*'/,
    marker,
  );
}
