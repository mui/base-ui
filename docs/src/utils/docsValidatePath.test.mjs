import { describe, expect, it } from 'vitest';
import {
  formatDocsValidateCommand,
  toDocsValidatePath,
  updateSectionIndexValidateMarker,
} from './docsValidatePath.mjs';

describe('docsValidatePath', () => {
  it('strips app prefixes and index file suffixes', () => {
    expect(toDocsValidatePath('src/app/(docs)/react/(components)/index.mdx')).toBe(
      '(docs)/react/(components)',
    );
    expect(toDocsValidatePath('src/app/(docs)/react/(components)/accordion/page.mdx')).toBe(
      '(docs)/react/(components)/accordion',
    );
  });

  it('quotes route-group paths', () => {
    expect(formatDocsValidateCommand('src/app/(docs)/react/(components)/index.mdx')).toBe(
      'pnpm docs:validate "(docs)/react/(components)"',
    );
  });

  it('replaces stale section validate paths', () => {
    const markdown = `[//]: # 'This section is autogenerated, DO NOT EDIT AFTER THIS LINE, run: pnpm docs:validate "(docs)/react/components"'`;

    expect(
      updateSectionIndexValidateMarker(markdown, 'src/app/(docs)/react/(components)/index.mdx'),
    ).toBe(
      `[//]: # 'This section is autogenerated, DO NOT EDIT AFTER THIS LINE, run: pnpm docs:validate "(docs)/react/(components)"'`,
    );
  });
});
