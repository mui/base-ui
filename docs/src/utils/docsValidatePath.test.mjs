import assert from 'node:assert/strict';
import test from 'node:test';
import {
  formatDocsValidateCommand,
  toDocsValidatePath,
  updateSectionIndexValidateMarker,
} from './docsValidatePath.mjs';

test('toDocsValidatePath strips app prefixes and index file suffixes', () => {
  assert.equal(
    toDocsValidatePath('src/app/(docs)/react/(components)/index.mdx'),
    '(docs)/react/(components)',
  );
  assert.equal(
    toDocsValidatePath('src/app/(docs)/react/(components)/accordion/page.mdx'),
    '(docs)/react/(components)/accordion',
  );
});

test('formatDocsValidateCommand quotes route-group paths', () => {
  assert.equal(
    formatDocsValidateCommand('src/app/(docs)/react/(components)/index.mdx'),
    'pnpm docs:validate "(docs)/react/(components)"',
  );
});

test('updateSectionIndexValidateMarker replaces stale section validate paths', () => {
  const markdown = `[//]: # 'This section is autogenerated, DO NOT EDIT AFTER THIS LINE, run: pnpm docs:validate "(docs)/react/components"'`;

  assert.equal(
    updateSectionIndexValidateMarker(
      markdown,
      'src/app/(docs)/react/(components)/index.mdx',
    ),
    `[//]: # 'This section is autogenerated, DO NOT EDIT AFTER THIS LINE, run: pnpm docs:validate "(docs)/react/(components)"'`,
  );
});
