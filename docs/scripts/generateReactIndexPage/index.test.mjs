import { describe, expect, it } from 'vitest';
import { generateReactIndexPage, isReactIndexPageCurrent } from './index.mjs';

describe('generateReactIndexPage', () => {
  it('uses a local generated marker that docs-infra does not rewrite', async () => {
    const markdown = await generateReactIndexPage();

    expect(markdown).toContain('Generated by pnpm --filter docs generate-react-index');
    expect(markdown).not.toContain('This section is autogenerated');
    expect(markdown).not.toContain('This file is autogenerated');
  });

  it('preserves section headings in order', async () => {
    const markdown = await generateReactIndexPage();

    expect(markdown.indexOf('## Overview')).toBeLessThan(markdown.indexOf('## Handbook'));
    expect(markdown.indexOf('## Handbook')).toBeLessThan(markdown.indexOf('## Components'));
    expect(markdown.indexOf('## Components')).toBeLessThan(markdown.indexOf('## Utilities'));
    expect(markdown.indexOf('## Utilities')).toBeLessThan(markdown.indexOf('### Quick start'));
  });

  it('separates the link index into sitemap sections', async () => {
    const markdown = await generateReactIndexPage();
    const linkIndex = markdown.slice(
      markdown.indexOf('## Overview'),
      markdown.indexOf('### Quick start'),
    );

    expect(linkIndex).toContain('## Overview\n\n- Quick start');
    expect(linkIndex).toContain('## Handbook\n\n- Styling');
    expect(linkIndex).toContain('## Components\n\n- Accordion');
    expect(linkIndex).toContain('## Utilities\n\n- CSP Provider');
  });

  it('preserves tags and outline links for child pages', async () => {
    const markdown = await generateReactIndexPage();

    expect(markdown).toContain(
      '- OTP Field [New] - ([Outline](#otp-field), [Contents](/react/otp-field))',
    );
    expect(markdown).toContain('- Dialog - ([Outline](#dialog), [Contents](/react/dialog))');
    expect(markdown).toContain(
      '- Quick start - ([Outline](#quick-start), [Contents](/react/quick-start))',
    );
  });

  it('includes detailed outline metadata for representative pages', async () => {
    const markdown = await generateReactIndexPage();

    expect(markdown).toContain('### Quick start');
    expect(markdown).toContain('- Sections:\n  - Install the library');
    expect(markdown).toContain('### Dialog');
    expect(markdown).toContain('- Exports:\n  - Dialog - Root');
    expect(markdown).toContain('### Direction Provider');
    expect(markdown).toContain('- Exports:\n  - DirectionProvider');
  });

  it('rewrites output links to canonical public React URLs', async () => {
    const markdown = await generateReactIndexPage();

    expect(markdown).toContain('[Read more](/react/dialog)');
    expect(markdown).toContain('[Read more](/react/about)');
    expect(markdown).not.toContain('/react/about-base-ui');
    expect(markdown).toContain('[Read more](/react/direction-provider)');
    expect(markdown).toContain(
      '- mergeProps - ([Outline](#mergeprops), [Contents](/react/merge-props))',
    );
    expect(markdown).toContain(
      '- useRender - ([Outline](#userender), [Contents](/react/use-render))',
    );
    expect(markdown).toContain('[Read more](/react/merge-props)');
    expect(markdown).toContain('[Read more](/react/use-render)');
    expect(markdown).toContain('- [llms.txt](/llms.txt) [External]');
    expect(markdown).not.toContain('/react/mergeprops');
    expect(markdown).not.toContain('/react/userender');
    expect(markdown).not.toContain('/react/llms-txt');
    expect(markdown).not.toMatch(/\/\((overview|handbook|components|utils)\)\//);
    expect(markdown).not.toMatch(/\.\/[^)\s]+\/page\.mdx/);
    expect(markdown).not.toMatch(/\/react\/(overview|handbook|components|utils)\//);
  });

  it('detects stale generated content', () => {
    expect(isReactIndexPageCurrent('stale\n', 'current\n')).toBe(false);
    expect(isReactIndexPageCurrent('current\n', 'current\n')).toBe(true);
  });
});
