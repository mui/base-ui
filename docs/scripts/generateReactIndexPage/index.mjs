#!/usr/bin/env node
/* eslint-disable no-console */

import fs from 'node:fs/promises';
import path from 'node:path';
import { pathToFileURL } from 'node:url';
import { loadServerSitemap } from '@mui/internal-docs-infra/pipeline/loadServerSitemap';

const DOCS_ROOT = path.resolve(import.meta.dirname, '../..');
const REACT_DOCS_ROOT = path.join(DOCS_ROOT, 'src/app/(docs)/react');
const OUTPUT_PATH = path.join(REACT_DOCS_ROOT, 'page.mdx');
const SITEMAP_PATH = path.join(DOCS_ROOT, 'src/app/sitemap/index.ts');

const SECTIONS = [
  { key: 'Overview', title: 'Overview' },
  { key: 'Handbook', title: 'Handbook' },
  { key: 'Components', title: 'Components' },
  { key: 'Utils', title: 'Utilities' },
];

const GENERATED_MARKER =
  "[//]: # 'Generated by pnpm --filter docs generate-react-index; do not edit this file manually.'";

function formatTags(tags = []) {
  return tags.map((tag) => ` [${tag}]`).join('');
}

function formatStatusLabel(page) {
  if (page.audience === 'private' && page.index) {
    return 'Index';
  }

  if (page.audience === 'private' && !page.index) {
    return 'Private';
  }

  if (page.audience && page.audience !== 'private') {
    const audience = page.audience.charAt(0).toUpperCase() + page.audience.slice(1);
    return page.index ? `${audience} Index` : audience;
  }

  if (page.index) {
    return 'Public Index';
  }

  return undefined;
}

function getCanonicalPath(page) {
  if (page.skipDetailSection) {
    return page.path;
  }

  const pagePath = page.path?.replace(/\\/g, '/');
  const pagePathMatch = pagePath?.match(/^(?:\.\/)?(.+)\/page\.mdx$/);

  return `/react/${pagePathMatch?.[1] ?? page.slug}`;
}

function renderPageListItem(page) {
  const title = page.displayTitle ?? page.title ?? page.slug;
  const path = getCanonicalPath(page);
  const tags = formatTags(page.tags);

  if (page.skipDetailSection) {
    return `- [${title}](${path})${tags}`;
  }

  const statusLabel = formatStatusLabel(page);
  const links = statusLabel
    ? `${statusLabel}, [Outline](#${page.slug}), [Contents](${path})`
    : `[Outline](#${page.slug}), [Contents](${path})`;

  return `- ${title}${tags} - (${links})`;
}

function renderHeadingHierarchy(hierarchy, depth = 0) {
  if (!hierarchy || Object.keys(hierarchy).length === 0) {
    return [];
  }

  return Object.values(hierarchy).flatMap((section) => {
    const indent = '  '.repeat(depth);
    return [`${indent}- ${section.title}`, ...renderHeadingHierarchy(section.children, depth + 1)];
  });
}

function renderMetadataEntries(title, entries) {
  if (!entries || Object.keys(entries).length === 0) {
    return [];
  }

  return Object.entries(entries).flatMap(([name, metadata]) => {
    const lines = [`  - ${title ? `${title} - ` : ''}${name}`];

    if (metadata.props?.length > 0) {
      lines.push(`    - Props: ${metadata.props.join(', ')}`);
    }

    if (metadata.parameters?.length > 0) {
      const parameters = metadata.parameters
        .map((parameter) => {
          return Array.isArray(parameter) ? `(${parameter.join(', ')})` : parameter;
        })
        .join(', ');

      lines.push(`    - Parameters: ${parameters}`);
    }

    if (metadata.returns?.length > 0) {
      lines.push(`    - Returns: ${metadata.returns.join(', ')}`);
    }

    if (metadata.dataAttributes?.length > 0) {
      lines.push(`    - Data Attributes: ${metadata.dataAttributes.join(', ')}`);
    }

    if (metadata.cssVariables?.length > 0) {
      lines.push(`    - CSS Variables: ${metadata.cssVariables.join(', ')}`);
    }

    return lines;
  });
}

function renderDetails(page) {
  const details = [];

  if (page.keywords?.length > 0) {
    details.push(`- Keywords: ${page.keywords.join(', ')}`);
  }

  const sections = renderHeadingHierarchy(page.sections, 1);
  if (sections.length > 0) {
    details.push(['- Sections:', ...sections].join('\n'));
  }

  const exports = [
    ...renderMetadataEntries(page.title, page.parts),
    ...renderMetadataEntries('', page.exports),
  ];
  if (exports.length > 0) {
    details.push(['- Exports:', ...exports].join('\n'));
  }

  if (page.types?.length > 0) {
    details.push(`- Types: ${page.types.join(', ')}`);
  }

  if (details.length === 0) {
    return '';
  }

  return [
    '<details>',
    '',
    '<summary>Outline</summary>',
    '',
    details.join('\n'),
    '',
    '</details>',
  ].join('\n');
}

function renderPageDetails(page) {
  if (page.skipDetailSection) {
    return '';
  }

  return [
    `### ${page.title ?? page.slug}`,
    page.description ?? 'No description available',
    renderDetails(page),
    `[Read more](${getCanonicalPath(page)})`,
  ]
    .filter(Boolean)
    .join('\n\n')
    .trimEnd();
}

function renderSection(sectionTitle, sectionMetadata) {
  const list = sectionMetadata.pages.map(renderPageListItem).join('\n');
  const details = sectionMetadata.pages.map(renderPageDetails).filter(Boolean).join('\n\n');

  return { details, links: `## ${sectionTitle}\n\n${list}` };
}

export async function generateReactIndexPage() {
  const sitemap = await loadServerSitemap(pathToFileURL(SITEMAP_PATH).href);

  const renderedSections = SECTIONS.map((section) => {
    const metadata = sitemap.data[section.key];
    if (metadata == null) {
      throw new Error(`Could not load sitemap section: ${section.key}`);
    }

    return renderSection(section.title, metadata);
  });
  const linkSections = renderedSections.map((section) => section.links).join('\n\n');
  const detailSections = renderedSections.map((section) => section.details).join('\n\n');

  return `${['# React', GENERATED_MARKER, linkSections, detailSections].join('\n\n').trimEnd()}\n`;
}

export function isReactIndexPageCurrent(currentContent, expectedContent) {
  return currentContent === expectedContent;
}

async function main() {
  const check = process.argv.includes('--check');
  const expectedContent = await generateReactIndexPage();

  if (check) {
    const currentContent = await fs.readFile(OUTPUT_PATH, 'utf-8');

    if (!isReactIndexPageCurrent(currentContent, expectedContent)) {
      console.error('docs/src/app/(docs)/react/page.mdx is out of date.');
      console.error('Run `pnpm --filter docs generate-react-index` and commit the result.');
      process.exit(1);
    }

    console.log('docs/src/app/(docs)/react/page.mdx is up to date.');
    return;
  }

  await fs.writeFile(OUTPUT_PATH, expectedContent, 'utf-8');
  console.log('Generated docs/src/app/(docs)/react/page.mdx');
}

if (import.meta.url === `file://${process.argv[1]}`) {
  main().catch((error) => {
    console.error(error);
    process.exit(1);
  });
}
