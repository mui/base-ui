export enum DateFieldSectionDataAttributes {
  /**
   * Index of the section in the field.
   */
  sectionIndex = 'data-section-index',
  /**
   * Present when the section value is empty.
   */
  empty = 'data-empty',
  /**
   * Present when the section is a separator (e.g. "/", "-").
   */
  separator = 'data-separator',
}
