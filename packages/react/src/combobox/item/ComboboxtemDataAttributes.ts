export enum ComboboxItemDataAttributes {
  /**
   * Present when the combobox item is selected.
   */
  selected = 'data-selected',
  /**
   * Present when the combobox item is highlighted.
   */
  highlighted = 'data-highlighted',
  /**
   * Present when the combobox item is disabled.
   */
  disabled = 'data-disabled',
}
